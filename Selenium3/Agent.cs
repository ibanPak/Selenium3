            int tries = 0;
            bool IsAppointmentPresent = false;
            while (IsAppointmentPresent == false)
            {
                try
                {
                    SeleniumSetMethods.Click(ElementType.Id, "searchButton1"); // refreshing results
                    tries = tries + 1;
